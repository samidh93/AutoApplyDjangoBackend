import boto3
from botocore.exceptions import ClientError
import json
import logging_config  # Import the logging configuration


def get_django_secret_key():

    secret_name = "django_secret_key"
    region_name = "eu-central-1"

    # Create a Secrets Manager client
    session = boto3.session.Session()
    client = session.client(
        service_name='secretsmanager',
        region_name=region_name
    )

    try:
        get_secret_value_response = client.get_secret_value(
            SecretId=secret_name
        )
    except ClientError as e:
        # For a list of exceptions thrown, see
        # https://docs.aws.amazon.com/secretsmanager/latest/apireference/API_GetSecretValue.html
        raise e

    secret_dict = json.loads(get_secret_value_response['SecretString'])["DJANGO_KEY"]
    # Your code goes here.
    return secret_dict

if __name__ == '__main__':
    get_django_secret_key()
